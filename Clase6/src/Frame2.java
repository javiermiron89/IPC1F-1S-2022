    private void Btt_EliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Btt_EliminarActionPerformed
        for (int i = 0; i < getListaUsuarios().length; i++) {
            if (getListaUsuarios()[i] != null) {
                if (getListaUsuarios()[i].getNombreUsuario().equals(Txt_Usuario.getText())) {
                    getListaUsuarios()[i] = null;
                }
            }
        }
    }//GEN-LAST:event_Btt_EliminarActionPerformed

    private void Btt_ReportarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Btt_ReportarActionPerformed
        String cadenaHTML = "<!DOCTYPE html>\n"
                + "<html lang=\"en\">\n"
                + "<head>\n"
                + "    <meta charset=\"UTF-8\">\n"
                + "    <meta http-equiv=\"X-UA-Compatible\" content=\"IE=edge\">\n"
                + "    <meta name=\"viewport\" content=\"width=device-width, initial-scale=1.0\">\n"
                + "    <title>Reporte</title>\n"
                + "</head>\n"
                + "<body>\n"
                + "<table>\n"
                + "        <tr>"
                + "          <th>Nombre Usuario</th>\n"
                + "          <th>Password</th>\n"
                + "        </tr>";

        for (int i = 0; i < getListaUsuarios().length; i++) {
            if (getListaUsuarios()[i] != null) {
                cadenaHTML += "<tr>";
                cadenaHTML += "  <td>" + getListaUsuarios()[i].getNombreUsuario() + "</td>";
                cadenaHTML += "  <td>" + getListaUsuarios()[i].getPassword()+ "</td>";
                cadenaHTML += "</tr>";
            }
        }

        cadenaHTML += "      </table>\n"
                + "</body>\n"
                + "</html>";

        File archivo = new File("reporte.html");
        try {
            BufferedWriter bw = new BufferedWriter(new FileWriter(archivo));
            bw.write(cadenaHTML);
            bw.close();
        } catch (Exception e) {
        }
    }//GEN-LAST:event_Btt_ReportarActionPerformed

