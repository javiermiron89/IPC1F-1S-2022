    private void Btt_AgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Btt_AgregarActionPerformed
        //Se verifica si hay que redimensionar el arreglo
        if (!hayEspacio(listaUsuarios)) {
            System.out.println("Tamaño Actual: " + getListaUsuarios().length);
            listaUsuarios = redimensionarArreglo(getListaUsuarios());
            System.out.println("Tamaño Nuevo: " + getListaUsuarios().length);
        }

        //Se busca una posicion vacia para agrear al suario
        for (int i = 0; i < getListaUsuarios().length; i++) {
            if (getListaUsuarios()[i] == null) {
                listaUsuarios[i] = new Usuario(Txt_Usuario.getText(), Txt_Password.getText());
                break;
            }
        }

        Txt_Usuario.setText("");
        Txt_Password.setText("");
    }//GEN-LAST:event_Btt_AgregarActionPerformed

    private void Btt_RefrescarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Btt_RefrescarActionPerformed
        for (int i = 0; i < getListaUsuarios().length; i++) {
            if (getListaUsuarios()[i] != null) {
                System.out.println("********");
                System.out.println(getListaUsuarios()[i].getNombreUsuario());
                System.out.println(getListaUsuarios()[i].getPassword());
                System.out.println("********");
            }
        }

        //PASAR DATOS A MATRIZ
        String[][] matriz = new String[getListaUsuarios().length][2];
        for (int i = 0; i < getListaUsuarios().length; i++) {
            if (getListaUsuarios()[i] != null) {
                matriz[i][0] = getListaUsuarios()[i].getNombreUsuario();
                matriz[i][1] = getListaUsuarios()[i].getPassword();
            }
        }

        //PASAR DATOS DE MATRIZ A TABLA
        String[] encabezado = {"Usuario", "Password"};
        Table_Principal.setModel(new DefaultTableModel(
                matriz,
                encabezado
        ));
    }//GEN-LAST:event_Btt_RefrescarActionPerformed

